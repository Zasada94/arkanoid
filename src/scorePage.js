export function scorePage(score) {
	let app = document.getElementById("app");
	app.innerHTML = `
<div id="scoreWrapper">
<div id="scoreDiv">${score}</div>
<textarea type="text" id="nick" name="nick" class="txtbox"  placeholder="Your Nick..." required></textarea>
        <button id="rankButton">
            <img id="rankButtonImg" src="./public/img/03/button_03_2.png">
        </button>
		<button id="playButton">
			<img id="playButtonImg" src="./public/img/03/button_03_1.png">
		</button>
</div>
`;

	const playButton = document.getElementById("playButton");
	const rankButton = document.getElementById("rankButton");
	const scoreWrapper = document.getElementById("scoreWrapper");
	const nick = document.getElementById("nick");
	const scoreDiv = document.getElementById("scoreDiv");
	const body = document.getElementById("body");

	function validate() {
		let result = true;
		if (nick.value.length < 3) {
			nick.classList.add("invalid");
			result = false;
		} else {
			for (let badword of badWordsArray) {
				if (nick.value.replace(/\s/gm, "").includes(badword)) {
					nick.classList.add("invalid");
					result = false;
				}
			}
		}
		if (result == true) {
			let nickValue = nick.value;
			rankPage(score, nickValue);
		}
		return result;
	}
	playButton.addEventListener("click", gamePage);
	rankButton.addEventListener("click", validate);

	let width;
	let windowWidth;
	const scorePageResize = () => {
		width = scoreWrapper.offsetWidth;
		windowWidth = body.offsetWidth;
		if (width >= windowWidth) {
			scoreDiv.classList.add("active");
			nick.classList.add("active");
		} else {
			scoreDiv.classList.remove("active");
			nick.classList.remove("active");
		}
	};
	scorePageResize();
	window.addEventListener("resize", scorePageResize);
}
